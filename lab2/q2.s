	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R1,=N2
	LDR R7,=RESULT
	MOV R4,#4
	MOV R5,#0
UP	LDR R2,[R0]
	LDR R3,[R1]
	ADDS R2,R5
	MOV R5,#0
	ADC R5,#0
	ADDS R2,R3
	ADC R5,#0
	STR R2,[R7],#4
	SUBS R4,#1
	BNE UP
STOP
	B STOP
N1 DCD 4,3,2,1
N2 DCD 2,3,4,5
	AREA mydata,DATA,READWRITE
RESULT DCD 0
	END