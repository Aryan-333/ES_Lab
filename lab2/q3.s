	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =VALUE1
	LDR R1, =VALUE2
	LDR R2,=RESULT
	LDR R3,[R0]
	LDR R4,[R1]
	SUBS R5,R3,R4
	STR R5,[R2]
STOP
	B STOP
VALUE1	DCD 8
VALUE2	DCD 3
	AREA mydata,DATA,READWRITE
RESULT DCD 0
	END